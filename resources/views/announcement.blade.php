  <script>
    function openAnnouncementModal() {
      document.getElementById('announcementModal').classList.remove('hidden');
      document.getElementById('announcementModal').classList.add('flex');
    }

    function closeAnnouncementModal() {
      document.getElementById('announcementModal').classList.add('hidden');
      document.getElementById('announcementModal').classList.remove('flex');
      document.getElementById('announcementForm').reset();
    }

    function handleAddAnnouncement(event) {
      event.preventDefault();
      
      const title = document.getElementById('eventTitle').value;
      const date = document.getElementById('eventDate').value;
      const venue = document.getElementById('eventVenue').value;
      const description = document.getElementById('eventDescription').value;
      
      // Format date
      const formattedDate = new Date(date).toLocaleDateString('en-US', {
        year: 'numeric',
        month: 'short',
        day: '2-digit'
      });
      
      // Create new announcement element
      const announcementHTML = `
        <article class="bg-white rounded-lg border-2 border-gray-300 p-6 shadow-sm hover:shadow-md transition-shadow">
          <div class="grid grid-cols-1 md:grid-cols-4 gap-6 items-start">
            <div class="md:col-span-1">
              <h3 class="font-semibold text-lg mb-2">Date</h3>
              <p class="text-gray-600">${formattedDate}</p>
            </div>
            
            <div class="md:col-span-2">
              <h3 class="font-semibold text-lg mb-2">Events and Announcements</h3>
              <h4 class="font-bold text-xl mb-3">${title}</h4>
              <p class="text-gray-700 leading-relaxed">${description}</p>
            </div>
            
            <div class="md:col-span-1">
              <h3 class="font-semibold text-lg mb-2">Venue</h3>
              <p class="text-gray-600">${venue}</p>
            </div>
          </div>
        </article>
      `;
      
      // Add to announcements list
      const announcementsList = document.querySelector('.space-y-6');
      announcementsList.insertAdjacentHTML('beforeend', announcementHTML);
      
      // Close modal and show success message
      closeAnnouncementModal();
      
      // Show success notification
      showNotification('Announcement added successfully!');
    }

    function showNotification(message) {
      const notification = document.createElement('div');
      notification.className = 'fixed top-4 right-4 bg-green-500 text-white px-6 py-3 rounded-lg shadow-lg z-50';
      notification.textContent = message;
      
      document.body.appendChild(notification);
      
      setTimeout(() => {
        notification.remove();
      }, 3000);
    }

    // Close modal when clicking outside
    document.getElementById('announcementModal').addEventListener('click', function(event) {
      if (event.target === this) {
        closeAnnouncementModal();
      }
    });

    // Close modal with Escape key
    document.addEventListener('keydown', function(event) {
      if (event.key === 'Escape') {
        closeAnnouncementModal();
      }
    });
  </script>
